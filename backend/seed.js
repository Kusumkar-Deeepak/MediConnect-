const seedDatabase = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI);
    console.log("Connected to MongoDB");

    await Hospital.deleteMany({});
    console.log("Cleared existing hospitals");

    await Client.deleteMany({});
    console.log("Cleared existing patients");

    const hashedHospitals = await Promise.all(
      hospitals.map(async (hospital) => {
        const hashedPassword = await bcrypt.hash(hospital.password, 10);
        return { ...hospital, password: hashedPassword };
      })
    );

    await Hospital.insertMany(hashedHospitals);
    console.log(`${hashedHospitals.length} hospitals inserted successfully`);

    const hashedPatients = await Promise.all(
      patients.map(async (patient) => {
        const hashedPassword = await bcrypt.hash(patient.password, 10);
        return { ...patient, password: hashedPassword };
      })
    );

    await Client.insertMany(hashedPatients);
    console.log(`${hashedPatients.length} patients inserted successfully`);

    console.log("\nâœ… Seed completed successfully!");
    console.log("\nHospital Login Credentials (ID/Password):");
    hospitals.forEach((h) => {
      console.log(`${h.id} / ${h.password}`);
    });

    console.log("\nPatient Login Credentials (Email/Password):");
    patients.forEach((p) => {
      console.log(`${p.email} / ${p.password}`);
    });

    process.exit(0);
  } catch (error) {
    console.error("Error seeding database:", error);
    process.exit(1);
  }
};

seedDatabase();
